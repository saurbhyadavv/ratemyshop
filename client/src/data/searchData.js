/* ── Helpers ───────────────────────────────────────────────────────────── */
export function getCityBySlug(slug) {
  return INDIAN_CITIES.find((c) => c.slug === slug) || null;
}

export function getCategoryBySlug(slug) {
  return SEO_CATEGORIES.find((c) => c.slug === slug) || null;
}

export function getShopTypeLabel(value) {
  const t = ALL_SHOP_TYPES.find((t) => t.value === value);
  return t ? t.label : 'Shop';
}

export function filterCities(query) {
  const q = query.toLowerCase().trim();
  if (!q) return [];
  return INDIAN_CITIES.filter((c) => c.name.toLowerCase().startsWith(q)).slice(0, 8);
}
